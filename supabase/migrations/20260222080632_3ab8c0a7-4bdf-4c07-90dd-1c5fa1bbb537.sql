## O julgamento que vai explodir sua cabeça

![Tribunal em debate](PLACEHOLDER_IMAGEM_3)

O caso chega à **Suprema Corte de Newgarth**, e é aqui que o livro fica absolutamente genial. **Cinco juízes analisam o mesmo caso — e chegam a conclusões completamente diferentes.** Cada voto é uma aula magistral de uma corrente do pensamento jurídico:

### ⚖️ Juiz Truepenny (Presidente)
Reconhece que a lei é clara: homicídio é homicídio. Mas sugere que o Poder Executivo conceda clemência. É a posição do **positivista moderado** — aplica a lei, mas reconhece que ela pode ser injusta.

### ⚖️ Juiz Foster
Defende a absolvição com dois argumentos brilhantes: primeiro, dentro da caverna os réus estavam em um **"estado de natureza"**, fora do alcance das leis da sociedade. Segundo, o **espírito da lei** de homicídio nunca pretendeu punir quem mata em legítima defesa da própria sobrevivência. É a voz do **jusnaturalismo** e da **interpretação teleológica**.

### ⚖️ Juiz Tatting
Fica tão dividido que **se declara impedido de votar**. Ele vê mérito nos argumentos de ambos os lados e não consegue decidir. Representa o **conflito moral** que todo jurista enfrenta quando a lei e a justiça apontam para direções opostas.

### ⚖️ Juiz Keen
Vota pela condenação com firmeza. Para ele, **juiz não faz política, juiz aplica a lei**. A lei diz que matar é crime, ponto final. Se a lei é injusta, que o Legislativo a mude. É o **positivismo jurídico puro**, a separação rígida entre Direito e Moral.

### ⚖️ Juiz Handy
Defende a absolvição usando o **senso comum e a opinião pública**. Pesquisas mostram que 90% da população absolveria os réus. Para ele, o Direito deve servir ao povo, não a abstrações teóricas. Representa o **realismo jurídico**.

